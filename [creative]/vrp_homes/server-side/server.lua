function cRP.paymentTheft(mobile)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local randItem = math.random(#mobileTheft[mobile])
		if vRP.computeInvWeight(user_id) + vRP.itemWeightList(mobileTheft[mobile][randItem][1]) * parseInt(mobileTheft[mobile][randItem][2]) <= vRP.getBackpack(user_id) then
			if math.random(100) <= 90 then
				vRP.giveInventoryItem(user_id,mobileTheft[mobile][randItem][1],parseInt(mobileTheft[mobile][randItem][2]),true)
			else
				TriggerClientEvent("Notify",source,"importante","Compartimento vazio.",5000)
			end
		else
			TriggerClientEvent("Notify",source,"negado","Mochila cheia.",5000)
		end

		vRP.upgradeStress(user_id,1)

		if math.random(100) >= 80 then
			vRP.wantedTimer(parseInt(user_id),120)
			TriggerClientEvent("Notify",source,"importante","As autoridades foram notificadas da tentativa de roubo.",5000)
			local x,y,z = vRPclient.getPositions(source)
			local copAmount = vRP.numPermission("Police")
			for k,v in pairs(copAmount) do
				async(function()
					TriggerClientEvent("NotifyPush",v,{ code = 90, title = "Roubo a Residncia", x = x, y = y, z = z, criminal = "Alarme de segurana", rgba = {160,108,15} })
				end)
			end
		end
	end
end