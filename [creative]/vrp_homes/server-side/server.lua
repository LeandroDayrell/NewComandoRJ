							permissoes = permissoes.."<b>Name:</b> "..identity.name.." "..identity.name2.."-<b>Passport:</b> "..v.user_id
							if k ~= #userHomes then
								permissoes = permissoes.."<br>"
							end
						end
					end
					TriggerClientEvent("Notify",source,"negado","Autorizaes de residncia <b>"..tostring(args[2]).."</b>: <br>"..permissoes,20000)
				else
					TriggerClientEvent("Notify",source,"negado","Nenhuma permisso encontrada.",5000)
				end
			end
		elseif args[1] == "sell" and homes[tostring(args[2])] then
			local myHomes = vRP.query("vRP/get_homeuserowner",{ user_id = parseInt(user_id), home = tostring(args[2]) })
			if myHomes[1] then
