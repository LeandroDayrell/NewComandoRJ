					if ok then
						local myResult = vRP.query("vRP/get_homeuser",{ user_id = parseInt(args[3]), home = tostring(args[2]) })
						if myResult[1] then
							TriggerClientEvent("Notify",source,"negado","A pessoa j possui uma permisso nesta residncia.",5000)
						else
							vRP.execute("vRP/transfer_homes",{ home = tostring(args[2]), user_id = parseInt(user_id), nuser_id = parseInt(args[3]) })
							TriggerClientEvent("Notify",source,"importante","Residncia transferida <b>"..tostring(args[2]).."</b> para <b>"..identity.name.." "..identity.name2.."</b>.",5000)
						end
					end
				end
			end
		else
			local myHomes = vRP.query("vRP/get_homeuserid",{ user_id = parseInt(user_id) })
			if parseInt(#myHomes) >= 1 then
				for k,v in pairs(myHomes) do
					TriggerClientEvent("Notify",source,"importante","<b>Residncia:</b> "..v.home,20000)
				end
			end
		end
	end
