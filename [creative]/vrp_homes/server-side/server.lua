RegisterCommand("homes",function(source,args,rawCommand)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		if args[1] == "add" and homes[tostring(args[2])] and parseInt(args[3]) > 0 then
			local myHomes = vRP.query("vRP/get_homeuserowner",{ user_id = parseInt(user_id), home = tostring(args[2]) })
			if myHomes[1] then
				local totalResidents = vRP.query("vRP/count_homepermissions",{ home = tostring(args[2]) })
				if parseInt(totalResidents[1].qtd) >= parseInt(homes[tostring(args[2])][3]) then
					TriggerClientEvent("Notify",source,"negado","A residncia "..tostring(args[2]).." atingiu o mximo de moradores.",5000)
					return
				end

				local identity = vRP.getUserIdentity(parseInt(args[3]))
				local totalHomes = vRP.query("vRP/count_homes",{ user_id = parseInt(args[3]) })
				if vRP.getPremium(parseInt(args[3])) then
					if parseInt(totalHomes[1].qtd) >= 4 then
						TriggerClientEvent("Notify",source,"negado","<b>"..identity.name.." "..identity.name2.."</b> atingiu o mximo de residncias.",5000)
						return
					end
				else
					if parseInt(totalHomes[1].qtd) >= 2 then
						TriggerClientEvent("Notify",source,"negado","<b>"..identity.name.." "..identity.name2.."</b> atingiu o mximo de residncias.",5000)
						return
					end
				end

				vRP.execute("vRP/add_permissions",{ home = tostring(args[2]), user_id = parseInt(args[3]) })
				if identity then
					TriggerClientEvent("Notify",source,"sucesso","Permisso de residncia <b>"..tostring(args[2]).."</b> adicionada para <b>"..identity.name.." "..identity.name2.."</b>.",5000)
				end
			end
		elseif args[1] == "rem" and homes[tostring(args[2])] and parseInt(args[3]) > 0 then
			local myHomes = vRP.query("vRP/get_homeuserowner",{ user_id = parseInt(user_id), home = tostring(args[2]) })
			if myHomes[1] then
				local userHomes = vRP.query("vRP/get_homeuser",{ user_id = parseInt(args[3]), home = tostring(args[2]) })
				if userHomes[1] then
					vRP.execute("vRP/rem_permissions",{ home = tostring(args[2]), user_id = parseInt(args[3]) })
					local identity = vRP.getUserIdentity(parseInt(args[3]))
					if identity then
						TriggerClientEvent("Notify",source,"importante","Permisso de residncia <b>"..tostring(args[2]).."</b> removida de <b>"..identity.name.." "..identity.name2.."</b>.",5000)
					end
				end
			end
		elseif args[1] == "vault" and homes[tostring(args[2])] then
			local myHomes = vRP.query("vRP/get_homeuserowner",{ user_id = parseInt(user_id), home = tostring(args[2]) })
			if myHomes[1] then
				if vRP.paymentBank(user_id,100000) then
					vRP.execute("vRP/upd_vaulthomes",{ home = tostring(args[2]), vault = 50 })
					TriggerClientEvent("Notify",source,"sucesso","Compra de espao efetuada com sucesso.",5000)
				else
					TriggerClientEvent("Notify",source,"negado","Dinheiro insuficiente.",5000)
				end
			end
		elseif args[1] == "check" and homes[tostring(args[2])] then
			local myHomes = vRP.query("vRP/get_homeuserowner",{ user_id = parseInt(user_id), home = tostring(args[2]) })
			if myHomes[1] then
				local userHomes = vRP.query("vRP/get_homepermissions",{ home = tostring(args[2]) })
				if parseInt(#userHomes) > 1 then
					local permissoes = ""
					for k,v in pairs(userHomes) do
						if v.user_id ~= user_id then
